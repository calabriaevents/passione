// Current language (default Italian)
let currentLanguage = localStorage.getItem('passione-language') || 'it';

// Initialize language on page load
document.addEventListener('DOMContentLoaded', function() {
    setLanguage(currentLanguage);
    updateLanguageButtons();
});

// Change language function
function changeLanguage(lang) {
    currentLanguage = lang;
    localStorage.setItem('passione-language', lang);
    setLanguage(lang);
    updateLanguageButtons();
    
    // Show language change notification
    showLanguageChangeNotification(lang);
}

// Set language for all translatable elements
function setLanguage(lang) {
    const elements = document.querySelectorAll('[data-translate]');
    
    elements.forEach(element => {
        const key = element.getAttribute('data-translate');
        const translation = translations[lang] && translations[lang][key];
        
        if (translation) {
            // Handle different element types
            if (element.tagName === 'INPUT') {
                if (element.type === 'text' || element.type === 'email') {
                    element.placeholder = translation;
                } else {
                    element.value = translation;
                }
            } else {
                element.textContent = translation;
            }
        }
    });
    
    // Update HTML lang attribute
    document.documentElement.lang = lang;
}

// Update active language button
function updateLanguageButtons() {
    const languageButtons = document.querySelectorAll('.language-btn');
    
    languageButtons.forEach(button => {
        const buttonLang = button.getAttribute('data-lang');
        if (buttonLang === currentLanguage) {
            button.classList.add('bg-white/20');
            button.style.opacity = '1';
        } else {
            button.classList.remove('bg-white/20');
            button.style.opacity = '0.7';
        }
    });
}

